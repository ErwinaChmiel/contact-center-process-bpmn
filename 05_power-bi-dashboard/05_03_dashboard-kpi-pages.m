# Wnioski biznesowe z dashboardu Power BI

## Cel dokumentu

Dokument opisuje, jakie wnioski biznesowe można uzyskać na podstawie dashboardu Power BI przygotowanego dla procesu Contact Center.

Dashboard nie pełni wyłącznie funkcji wizualizacji danych. Jego celem jest wsparcie decyzji operacyjnych i zarządczych.

---

## Rola dashboardu

Dashboard Power BI został przygotowany jako narzędzie wspierające:

- liderów zespołów,
- menedżerów Contact Center,
- analityków operacyjnych,
- osoby odpowiedzialne za jakość obsługi klienta.

Raport umożliwia analizę efektywności procesu obsługi połączeń, monitorowanie KPI oraz identyfikację wyjątków wymagających reakcji.

---

## Kluczowe obszary analizy

| Obszar | Opis |
|---|---|
| Wolumen połączeń | Analiza liczby połączeń przychodzących |
| Czas oczekiwania | Monitorowanie ASA |
| Czas obsługi | Monitorowanie AHT |
| Porzucone połączenia | Analiza Abandonment Rate |
| Skuteczność obsługi | Analiza FCR |
| SLA | Monitorowanie terminowości obsługi |
| Callback | Analiza skuteczności oddzwonień |
| Self-service | Ocena udziału spraw obsłużonych automatycznie |
| Efektywność konsultantów | Analiza KPI per agent |
| Alerty operacyjne | Identyfikacja wyjątków wymagających reakcji |

---

## Przykładowe pytania biznesowe

Dashboard odpowiada na pytania takie jak:

- W których godzinach Contact Center ma największe obciążenie?
- Jaki jest poziom SLA w analizowanym okresie?
- Czy FCR spełnia oczekiwany poziom?
- Ilu klientów porzuca połączenie przed kontaktem z konsultantem?
- Czy callbacki są realizowane terminowo?
- Które kategorie spraw najczęściej wymagają eskalacji?
- Którzy konsultanci mają podwyższony AHT?
- Czy self-service odciąża konsultantów?

---

## Interpretacja KPI

| KPI | Interpretacja biznesowa |
|---|---|
| ASA | Wysoka wartość oznacza długi czas oczekiwania klienta |
| AHT | Wysoka wartość może wskazywać na złożone sprawy lub problemy procesowe |
| FCR | Niska wartość oznacza, że sprawy często wymagają ponownego kontaktu lub eskalacji |
| SLA | Niska wartość wskazuje na ryzyko niedotrzymania terminów obsługi |
| Abandonment Rate | Wysoka wartość oznacza, że klienci rezygnują z oczekiwania |
| Callback Realization Rate | Niska wartość wskazuje na problemy z realizacją oddzwonień |
| Self-service Rate | Wysoka wartość oznacza większy udział automatycznej obsługi prostych spraw |

---

## Możliwe decyzje operacyjne

| Obserwacja w dashboardzie | Możliwa decyzja |
|---|---|
| Wysokie ASA w określonych godzinach | Zmiana grafików lub zwiększenie obsady |
| Wysoki Abandonment Rate | Uruchomienie lub promowanie callbacku |
| Niski FCR | Analiza jakości obsługi i przyczyn eskalacji |
| Wysoki AHT u części konsultantów | Dodatkowe szkolenie lub analiza kategorii spraw |
| Duża liczba spraw po SLA | Priorytetyzacja zaległych zgłoszeń |
| Niski Self-service Rate | Rozbudowa IVR lub automatyzacji |
| Niezrealizowane callbacki | Monitoring procesu oddzwonień i odpowiedzialności zespołu |

---

## Wartość biznesowa dashboardu

Dashboard wspiera organizację w:

- szybszym wykrywaniu problemów operacyjnych,
- monitorowaniu jakości obsługi klienta,
- analizie skuteczności procesu TO-BE,
- lepszym planowaniu zasobów,
- ograniczaniu porzuconych połączeń,
- poprawie terminowości obsługi,
- podejmowaniu decyzji na podstawie danych.

---

## Powiązanie dashboardu z analizą biznesową

Dashboard Power BI jest końcowym elementem procesu analitycznego.

Wynika bezpośrednio z:

- problemów biznesowych,
- wymagań,
- User Stories,
- kryteriów akceptacji,
- procesu BPMN TO-BE,
- modelu danych SQL,
- integracji systemowych.

Dzięki temu raport nie jest oderwaną wizualizacją, ale narzędziem mierzącym efektywność zaproponowanych usprawnień.

---

## Podsumowanie

Dashboard Power BI pozwala ocenić, czy proces TO-BE odpowiada na problemy zidentyfikowane w procesie AS-IS.

Najważniejszą wartością dashboardu jest możliwość przejścia od danych operacyjnych do decyzji biznesowych.


